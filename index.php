        document.addEventListener('DOMContentLoaded', function() {

            // ===== SCROLL REVEAL =====
            const reveals = document.querySelectorAll('.reveal');

            function checkReveal() {
                const trigger = window.innerHeight * 0.85;
                reveals.forEach(el => {
                    const rect = el.getBoundingClientRect();
                    if (rect.top < trigger && rect.bottom > 0) {
                        el.classList.add('visible');
                    }
                });
            }

            window.addEventListener('scroll', checkReveal);
            window.addEventListener('resize', checkReveal);
            checkReveal();

            // ===== STEP REVEAL =====
            const steps = document.querySelectorAll('.step-item');

            function checkSteps() {
                const trigger = window.innerHeight * 0.85;
                steps.forEach(el => {
                    const rect = el.getBoundingClientRect();
                    if (rect.top < trigger && rect.bottom > 0) {
                        const delay = parseInt(el.dataset.delay) || 0;
                        setTimeout(() => {
                            el.classList.add('visible');
                        }, delay);
                    }
                });
            }

            window.addEventListener('scroll', checkSteps);
            window.addEventListener('resize', checkSteps);
            checkSteps();

            // ===== FAQ TOGGLE =====
            const faqItems = document.querySelectorAll('.faq-item');

            faqItems.forEach(item => {
                const question = item.querySelector('.question');
                question.addEventListener('click', function(e) {
                    const isActive = item.classList.contains('active');
                    faqItems.forEach(i => i.classList.remove('active'));
                    if (!isActive) item.classList.add('active');
                });
            });

            // ===== QUANTITY BOX =====
            document.querySelectorAll('.qty-box').forEach(box => {
                const minus = box.querySelector('.qty-minus');
                const plus = box.querySelector('.qty-plus');
                const input = box.querySelector('.qty-input');

                if (minus && plus && input) {
                    minus.addEventListener('click', function() {
                        let val = parseInt(input.value);
                        if (val > 1) input.value = val - 1;
                    });

                    plus.addEventListener('click', function() {
                        let val = parseInt(input.value);
                        input.value = val + 1;
                    });
                }
            });

            // ===== ADD TO CART =====
            document.querySelectorAll('.add-to-cart').forEach(btn => {
                btn.addEventListener('click', function(e) {
                    e.preventDefault();
                    const product = this.dataset.product;
                    const price = this.dataset.price;
                    const card = this.closest('.pricing-card');
                    const qtyInput = card ? card.querySelector('.qty-input') : null;
                    const qty = qtyInput ? parseInt(qtyInput.value) : 1;

                    const url = `cart/add_to_cart.php?product=${encodeURIComponent(product)}&price=${price}&qty=${qty}`;
                    window.location.href = url;
                });
            });

        });
    </script>

